function Chapter({ title, children }: { title: string; children: React.ReactNode }) {
  return (
    <section className="space-y-5">
      <h2 className="border-b border-border pb-2 text-xl font-bold text-foreground md:text-2xl">
        {title}
      </h2>
      {children}
    </section>
  )
}

function Article({ title, children }: { title: string; children: React.ReactNode }) {
  return (
    <article className="space-y-2">
      <h3 className="text-base font-semibold text-foreground md:text-lg">{title}</h3>
      <div className="space-y-2 pl-1">{children}</div>
    </article>
  )
}

function Ol({ children, type }: { children: React.ReactNode; type?: "alpha" }) {
  const cls =
    type === "alpha"
      ? "ml-5 list-[lower-alpha] space-y-1"
      : "ml-5 list-decimal space-y-1.5"
  return <ol className={cls}>{children}</ol>
}
